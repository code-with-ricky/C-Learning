#include<stdio.h>
#include<math.h>

int main(){
    // 1. sqrt()
    printf("%f\n", sqrt(25));

    // 2. ceil(): roundes the number upward to nearest integer
    printf("%f\n", ceil(1.4));
    
    // 3. floor(): roundes the number lower to nearest integer
    printf("%f\n", floor(1.4));

    // 4. pow(x, y): return x to the power y
    printf("%f\n", pow(2, 3));

    return 0;
}